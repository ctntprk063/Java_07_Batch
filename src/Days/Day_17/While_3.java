package Days.Day_17;

public class While_3 {
    public static void main(String[] args) {

        int a = 5;
        while (a > 0) {
            System.out.println(a);
            a--;
        }
// 50 ye kadar olan cift sayilar
        int b = 0;
        while (b <= 50) {
            System.out.println(b);
            b += 2;
        }


    }
}
